package loveProgramming;

public class MathOperations {

    public static void main(String[] args) {
        int a, b, c;
        a = 125;
        b = 24;
        c = a+b;
        System.out.println ("sum of "+ a + " and "+ b + " is " + c);


        c = a-b;
        System.out.println (c);


        c = a * b;
        System.out.println(c);
        c = a/b;
        System.out.println(c);
        c = a % b;
        System.out.println(c);


    }
}
